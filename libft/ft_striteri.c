#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	index;

	index = 0;
	if (f && s)
	{
		while (s[index])
		{
			(*f)(index, &s[index]);
			index++;
		}
	}
}
